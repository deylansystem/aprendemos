// Modificar el código de voces
let voices = [];
const voiceSelect = document.getElementById('voiceSelect');

function loadVoices() {
voices = speechSynthesis.getVoices();
voiceSelect.innerHTML = '<option value="auto">Voz automática</option>';

// Filtrar solo voces en español con alta prioridad
const spanishVoices = voices.filter(voice => {
return voice.lang === 'es-ES' || 
       voice.lang === 'es-MX' ||
       voice.lang === 'es-US' ||
       voice.lang === 'es-AR' ||
       voice.lang.startsWith('es-');
});

// Ordenar por calidad
const preferredVoices = spanishVoices.sort((a, b) => {
const qualityOrder = ['Google español', 'Microsoft Helena', 'Microsoft Sabina', 'Microsoft Pablo'];
return qualityOrder.indexOf(b.name) - qualityOrder.indexOf(a.name);
});

preferredVoices.forEach((voice, index) => {
const option = document.createElement('option');
option.value = index;
option.textContent = `${voice.name} (${voice.lang.replace('_', '-')})`;
voiceSelect.appendChild(option);
});

// Si no hay voces en español, mostrar advertencia
if (spanishVoices.length === 0) {
const option = document.createElement('option');
option.textContent = "No se encontraron voces en español";
option.disabled = true;
voiceSelect.appendChild(option);
}
}

function speak(text) {
if (currentUtterance) {
speechSynthesis.cancel();
}

currentUtterance = new SpeechSynthesisUtterance(text);
currentUtterance.rate = 0.4;  // Velocidad ligeramente más natural
currentUtterance.pitch = 1;   // Tono neutral

// Forzar siempre español aunque la voz no lo especifique
currentUtterance.lang = 'es-ES';

// Configurar voz seleccionada si existe
const selectedVoice = voiceSelect.value;
if (selectedVoice !== 'auto' && voices[selectedVoice]) {
currentUtterance.voice = voices[selectedVoice];
currentUtterance.lang = voices[selectedVoice].lang;
}

// Configuraciones adicionales para mejor pronunciación
currentUtterance.text = text
.replace(/¿/g, '')  // Remover signos de interrogación iniciales
.replace(/¡/g, '') // Remover signos de exclamación iniciales
.replace(/:/g, '')  // Eliminar los dos puntos
.replace(/¿/g, '')  // Remover signos de interrogación iniciales
.replace(/¡/g, '')  // Remover signos de exclamación iniciales
.replace(/\s+/g, ' ').trim(); 

speechSynthesis.speak(currentUtterance);
}

// Inicialización mejorada de voces
if (speechSynthesis.onvoiceschanged !== undefined) {
speechSynthesis.onvoiceschanged = loadVoices;
}

// Cargar voces cada segundo hasta que se detecten (para móviles)
const voiceCheckInterval = setInterval(() => {
if (speechSynthesis.getVoices().length > 0) {
loadVoices();
clearInterval(voiceCheckInterval);
}
}, 1000);

// Cargar inicialmente
loadVoices();



